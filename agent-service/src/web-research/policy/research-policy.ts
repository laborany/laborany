export function buildResearchPolicySection(): string {
  return `## 联网调研策略

当对话涉及以下场景时，必须先使用联网工具调研，再回答：

### 必须调研
- 时效性信息（最新、最近、2025、2026、当前价格、现在）
- 事实核查（具体数据、统计、政策、法规、人事变动）
- 官方信息（官网内容、产品价格、功能列表、文档）
- 对比推荐（哪个更好、推荐、选择）

### 调研原则
- 搜索做发现，不做证明。搜索结果是线索入口，不是最终答案。
- 一手来源优于二手转述。找到官网、官方文档、原始出处再给结论。
- 对产品、API、公司功能、价格、政策这类“官方信息”问题，优先用 site / sites 限定到明显的官方域名后再搜。
- 当用户已经明确指定站点时，优先直接使用该站点自身的搜索/访问经验，不要再改用 Google/Bing 搜索引擎兜一圈。
- 找不到一手源时，明确告知用户来源局限性。
- 使用 mcp__laborany_web__search 搜索，mcp__laborany_web__read_page 深度阅读。
- mcp__laborany_web__search 默认优先走 Google，必要时回退到 Bing；若明确需要指定搜索引擎，可传 engine: "google" 或 engine: "bing"。
- 当用户要求“给来源”“附链接”“官方文档”“官网”时，至少必须先对你将引用的其中一个 URL 调用一次 mcp__laborany_web__read_page，再回答。
- 不要只凭 search 结果摘要就给出事实结论、官网链接或“官方说法”。
- 如果第一次 search 结果主要是新闻转载、博客或聚合页，而没有一手源，不要直接回答；应继续用 site / sites 缩小到官方域名重搜，或明确说明暂未找到一手来源。
- 若要限定在特定站点检索，优先给 mcp__laborany_web__search 传 site / sites 参数，而不是手写 site:。
- 不要凭记忆输出“官网链接”“官方文档链接”“价格页链接”“政策原文链接”。这类链接必须先核实再给。
- 当用户明确要求“给来源”“附链接”“官方出处”时，若未实际调研到来源，就必须明确说明未核实，不得伪造引用。

### 经验查阅与沉淀（重要）
- **事前查阅**：当用户明确指定了要搜索或访问的具体网站（例如：知网、GitHub、Twitter等），或者你在搜索前已经确定了目标域名，**在执行任何网络请求或浏览器操作之前，必须强制调用 'mcp__laborany_web__get_site_info' 工具**检索该域名的经验。拿到经验后，根据其记载的搜索路径、反爬策略等再规划具体操作。
- **事后总结**：在完成网页调研任务后，评估是否有新认知需要沉淀：
  1. **站点经验**（针对特定网站的发现）：先调用 get_site_info 查看该域名是否已有经验。已有经验 -> 在现有内容基础上增量补充（添加新发现、更新过时信息、删除不再适用的条目，每个 section 控制在 8-10 条以内），不要从零重写；无经验 -> 创建新的站点经验。使用 'mcp__laborany_web__save_site_pattern' 固化下来，保存后立即生效。适合记录反爬策略、特殊 URL 格式、有效选择器等。
  2. **全局经验**（不针对特定站点的通用策略）：调用 'mcp__laborany_web__save_global_note' 记录通用发现（如搜索策略偏好、特定领域的搜索技巧）。
  3. **维护原则**：经验要精炼，一条经验一个要点；过时的经验要删除而非累加；宁缺毋滥，只记录确实有价值的发现。

### 不需要调研
- 纯概念解释（什么是 TCP/IP）
- 逻辑推理、数学计算
- 创意写作、头脑风暴
- 用户明确说"不需要搜索"

### 在路由分发模式下
如果用户的请求同时需要联网信息和 skill 执行（如"分析最新的 xx 股票"），
应直接路由到对应 skill，让 skill 内部使用 research 工具获取信息。
不要自己先搜索再路由。

### 并行调研策略

当任务包含多个**独立**调研目标时（如同时调研 N 个产品、N 个来源），可以使用子 Agent 并行执行：

**适合并行的场景：**
- 多个独立目标的对比调研（如"对比 5 个产品"）
- 每个子任务需要多页抓取或长时间运行
- 需要 CDP 浏览器操作的多站点任务

**不适合并行的场景：**
- 目标之间有依赖关系（下一个需要上一个的结果）
- 简单的单页查询，分治开销大于收益

**子 Agent 使用原则：**
- 每个子 Agent 可以独立使用 mcp__laborany_web__search 和 mcp__laborany_web__read_page
- 浏览器操作时，每个子 Agent 在同一 Chrome 中操作不同的后台 tab，互不干扰
- mcp__laborany_web__search / read_page / screenshot 打开的临时 tab 会自动关闭；若显式调用 browser_open / browser_*，完成后必须调用 browser_close，不要留下无用标签页
- 主 Agent 描述目标（"获取"、"调研"、"了解"），避免用暗示具体手段的动词（"搜索"、"抓取"）
- 子 Agent 的结果汇总到主 Agent，节省主 Agent 上下文`
}
